void SDFModel::ApplyPose(const std::unordered_map<std::string, SDFTransform>& pose) {
    if (!m_root) return;

    m_root->ForEach([&pose](SDFPrimitive& prim) {
        auto it = pose.find(prim.GetName());
        if (it != pose.end()) {
            prim.SetLocalTransform(it->second);
        }
    });

    InvalidateMesh();
}

std::unordered_map<std::string, SDFTransform> SDFModel::GetCurrentPose() const {
    std::unordered_map<std::string, SDFTransform> pose;
    if (m_root) {
        m_root->ForEach([&pose](const SDFPrimitive& prim) {
            if (!prim.GetName().empty()) {
                pose[prim.GetName()] = prim.GetLocalTransform();
            }
        });
    }
    return pose;
}

void SDFModel::ResetToBindPose() {
    ApplyPose(m_bindPose);
}

void SDFModel::SetBindPose() {
    m_bindPose = GetCurrentPose();
}

void SDFModel::CollectPrimitives(SDFPrimitive* primitive, std::vector<SDFPrimitive*>& out) {
    out.push_back(primitive);
    for (auto& child : primitive->GetChildren()) {
        CollectPrimitives(child.get(), out);
    }
}

void SDFModel::CollectPrimitives(const SDFPrimitive* primitive, std::vector<const SDFPrimitive*>& out) const {
    out.push_back(primitive);
    for (const auto& child : primitive->GetChildren()) {
        CollectPrimitives(child.get(), out);
    }
}

