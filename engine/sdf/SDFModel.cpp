PaintLayer* SDFModel::AddPaintLayer(const std::string& name, int width, int height) {
    PaintLayer layer;
    layer.name = name;
    layer.width = width;
    layer.height = height;
    layer.data.resize(static_cast<size_t>(width * height * 4), 255);  // RGBA white
    m_paintLayers.push_back(std::move(layer));
    return &m_paintLayers.back();
}

bool SDFModel::RemovePaintLayer(const std::string& name) {
    auto it = std::find_if(m_paintLayers.begin(), m_paintLayers.end(),
        [&name](const PaintLayer& layer) { return layer.name == name; });
    if (it != m_paintLayers.end()) {
        m_paintLayers.erase(it);
        return true;
    }
    return false;
}

PaintLayer* SDFModel::GetPaintLayer(const std::string& name) {
    for (auto& layer : m_paintLayers) {
        if (layer.name == name) return &layer;
    }
    return nullptr;
}

