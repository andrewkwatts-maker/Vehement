float SDFModel::EvaluateSDF(const glm::vec3& point) const {
    if (!m_root) return 1e10f;
    return EvaluateHierarchy(m_root.get(), point);
}

float SDFModel::EvaluateHierarchy(const SDFPrimitive* primitive, const glm::vec3& worldPoint) const {
    if (!primitive || !primitive->IsVisible()) return 1e10f;

    SDFTransform world = primitive->GetWorldTransform();
    glm::vec3 localPoint = world.InverseTransformPoint(worldPoint);
    float dist = primitive->EvaluateSDF(localPoint);

    // Combine with children
    for (const auto& child : primitive->GetChildren()) {
        float childDist = EvaluateHierarchy(child.get(), worldPoint);

        switch (child->GetCSGOperation()) {
            case CSGOperation::Union:
                dist = SDFEval::Union(dist, childDist);
                break;
            case CSGOperation::Subtraction:
                dist = SDFEval::Subtraction(childDist, dist);
                break;
            case CSGOperation::Intersection:
                dist = SDFEval::Intersection(dist, childDist);
                break;
            case CSGOperation::SmoothUnion:
                dist = SDFEval::SmoothUnion(dist, childDist, child->GetParameters().smoothness);
                break;
            case CSGOperation::SmoothSubtraction:
                dist = SDFEval::SmoothSubtraction(childDist, dist, child->GetParameters().smoothness);
                break;
            case CSGOperation::SmoothIntersection:
                dist = SDFEval::SmoothIntersection(dist, childDist, child->GetParameters().smoothness);
                break;
        }
    }

    return dist;
}

std::pair<glm::vec3, glm::vec3> SDFModel::GetBounds() const {
    glm::vec3 minBounds(1e10f);
    glm::vec3 maxBounds(-1e10f);

    if (m_root) {
        m_root->ForEach([&](const SDFPrimitive& prim) {
            SDFTransform world = prim.GetWorldTransform();
            auto [localMin, localMax] = prim.GetLocalBounds();

            // Transform 8 corners of local bounds to world space
            for (int i = 0; i < 8; ++i) {
                glm::vec3 corner(
                    (i & 1) ? localMax.x : localMin.x,
                    (i & 2) ? localMax.y : localMin.y,
                    (i & 4) ? localMax.z : localMin.z
                );
                glm::vec3 worldCorner = world.TransformPoint(corner);
                minBounds = glm::min(minBounds, worldCorner);
                maxBounds = glm::max(maxBounds, worldCorner);
            }
        });
    }

    return {minBounds, maxBounds};
}

glm::vec3 SDFModel::CalculateNormal(const glm::vec3& point, float epsilon) const {
    glm::vec3 normal;
    normal.x = EvaluateSDF(point + glm::vec3(epsilon, 0, 0)) - EvaluateSDF(point - glm::vec3(epsilon, 0, 0));
    normal.y = EvaluateSDF(point + glm::vec3(0, epsilon, 0)) - EvaluateSDF(point - glm::vec3(0, epsilon, 0));
    normal.z = EvaluateSDF(point + glm::vec3(0, 0, epsilon)) - EvaluateSDF(point - glm::vec3(0, 0, epsilon));
    return glm::normalize(normal);
}

std::shared_ptr<Mesh> SDFModel::GenerateMesh(const SDFMeshSettings& settings) const {
    auto [boundsMin, boundsMax] = GetBounds();

    // Add padding
    glm::vec3 padding(settings.boundsPadding);
    boundsMin -= padding;
    boundsMax += padding;

    auto sdfFunc = [this](const glm::vec3& p) { return EvaluateSDF(p); };
    return MarchingCubes::Generate(sdfFunc, boundsMin, boundsMax, settings);
}

