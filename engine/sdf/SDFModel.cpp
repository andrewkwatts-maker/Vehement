SDFPrimitive* SDFModel::CreatePrimitive(const std::string& name, SDFPrimitiveType type, SDFPrimitive* parent) {
    auto primitive = std::make_unique<SDFPrimitive>(name, type);
    SDFPrimitive* ptr = primitive.get();

    if (parent) {
        parent->AddChild(std::move(primitive));
    } else if (m_root) {
        m_root->AddChild(std::move(primitive));
    } else {
        m_root = std::move(primitive);
    }

    InvalidateMesh();

    if (OnPrimitiveAdded) {
        OnPrimitiveAdded(ptr);
    }

    return ptr;
}

SDFPrimitive* SDFModel::FindPrimitive(const std::string& name) {
    if (!m_root) return nullptr;
    if (m_root->GetName() == name) return m_root.get();
    return m_root->FindChild(name);
}

SDFPrimitive* SDFModel::FindPrimitiveById(uint32_t id) {
    if (!m_root) return nullptr;
    if (m_root->GetId() == id) return m_root.get();
    return m_root->FindChildById(id);
}

bool SDFModel::DeletePrimitive(SDFPrimitive* primitive) {
    if (!primitive || !m_root) return false;

    uint32_t id = primitive->GetId();

    if (primitive == m_root.get()) {
        m_root.reset();
        InvalidateMesh();
        if (OnPrimitiveRemoved) OnPrimitiveRemoved(id);
        return true;
    }

    SDFPrimitive* parent = primitive->GetParent();
    if (parent && parent->RemoveChild(primitive)) {
        InvalidateMesh();
        if (OnPrimitiveRemoved) OnPrimitiveRemoved(id);
        return true;
    }

    return false;
}

