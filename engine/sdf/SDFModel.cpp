std::shared_ptr<Mesh> Generate(
    const std::function<float(const glm::vec3&)>& sdf,
    const glm::vec3& boundsMin,
    const glm::vec3& boundsMax,
    const SDFMeshSettings& settings) {

    std::vector<glm::vec3> positions;
    std::vector<glm::vec3> normals;
    std::vector<uint32_t> indices;

    Polygonize(sdf, boundsMin, boundsMax, settings.resolution, settings.isoLevel, positions, normals, indices);

    if (positions.empty()) {
        return nullptr;
    }

    // Create mesh using Vertex struct
    auto mesh = std::make_shared<Mesh>();

    // Generate UVs (simple box projection for now)
    glm::vec3 size = boundsMax - boundsMin;

    // Build vertex list
    std::vector<Vertex> vertices;
    vertices.reserve(positions.size());

    for (size_t i = 0; i < positions.size(); ++i) {
        Vertex v;
        v.position = positions[i];
        v.normal = normals[i];

        if (settings.generateUVs) {
            glm::vec3 normalized = (positions[i] - boundsMin) / size;
            v.texCoords = glm::vec2(normalized.x, normalized.z);
        } else {
            v.texCoords = glm::vec2(0.0f);
        }

        v.tangent = glm::vec3(1.0f, 0.0f, 0.0f);
        v.bitangent = glm::vec3(0.0f, 0.0f, 1.0f);

        vertices.push_back(v);
    }

    // Create the mesh with vertices and indices
    mesh->Create(vertices, indices);

    return mesh;
}

void Polygonize(
    const std::function<float(const glm::vec3&)>& sdf,
    const glm::vec3& boundsMin,
    const glm::vec3& boundsMax,
    int resolution,
    float isoLevel,
    std::vector<glm::vec3>& outPositions,
    std::vector<glm::vec3>& outNormals,
    std::vector<uint32_t>& outIndices) {

    glm::vec3 size = boundsMax - boundsMin;
    glm::vec3 step = size / static_cast<float>(resolution);

    // Sample SDF at grid points
    std::vector<float> grid;
    grid.resize(static_cast<size_t>((resolution + 1) * (resolution + 1) * (resolution + 1)));

    auto gridIndex = [resolution](int x, int y, int z) {
        return static_cast<size_t>(x + y * (resolution + 1) + z * (resolution + 1) * (resolution + 1));
    };

    // Sample grid
    for (int z = 0; z <= resolution; ++z) {
        for (int y = 0; y <= resolution; ++y) {
            for (int x = 0; x <= resolution; ++x) {
                glm::vec3 pos = boundsMin + glm::vec3(x, y, z) * step;
                grid[gridIndex(x, y, z)] = sdf(pos);
            }
        }
    }

    // Edge interpolation helper
    auto interpolateVertex = [&](int x1, int y1, int z1, int x2, int y2, int z2) -> glm::vec3 {
        glm::vec3 p1 = boundsMin + glm::vec3(x1, y1, z1) * step;
        glm::vec3 p2 = boundsMin + glm::vec3(x2, y2, z2) * step;
        float v1 = grid[gridIndex(x1, y1, z1)];
        float v2 = grid[gridIndex(x2, y2, z2)];

        if (std::abs(isoLevel - v1) < 0.00001f) return p1;
        if (std::abs(isoLevel - v2) < 0.00001f) return p2;
        if (std::abs(v1 - v2) < 0.00001f) return p1;

        float t = (isoLevel - v1) / (v2 - v1);
        return p1 + t * (p2 - p1);
    };

    // Process each cube
    for (int z = 0; z < resolution; ++z) {
        for (int y = 0; y < resolution; ++y) {
            for (int x = 0; x < resolution; ++x) {
                // Get cube corner values
                float cubeValues[8];
                cubeValues[0] = grid[gridIndex(x, y, z)];
                cubeValues[1] = grid[gridIndex(x + 1, y, z)];
                cubeValues[2] = grid[gridIndex(x + 1, y + 1, z)];
                cubeValues[3] = grid[gridIndex(x, y + 1, z)];
                cubeValues[4] = grid[gridIndex(x, y, z + 1)];
                cubeValues[5] = grid[gridIndex(x + 1, y, z + 1)];
                cubeValues[6] = grid[gridIndex(x + 1, y + 1, z + 1)];
                cubeValues[7] = grid[gridIndex(x, y + 1, z + 1)];

                // Determine cube index
                int cubeIndex = 0;
                for (int i = 0; i < 8; ++i) {
                    if (cubeValues[i] < isoLevel) cubeIndex |= (1 << i);
                }

                // Skip if cube is entirely inside or outside
                if (edgeTable[cubeIndex] == 0) continue;

                // Interpolate vertices on edges
                glm::vec3 vertList[12];

                if (edgeTable[cubeIndex] & 1)
                    vertList[0] = interpolateVertex(x, y, z, x + 1, y, z);
                if (edgeTable[cubeIndex] & 2)
                    vertList[1] = interpolateVertex(x + 1, y, z, x + 1, y + 1, z);
                if (edgeTable[cubeIndex] & 4)
                    vertList[2] = interpolateVertex(x + 1, y + 1, z, x, y + 1, z);
                if (edgeTable[cubeIndex] & 8)
                    vertList[3] = interpolateVertex(x, y + 1, z, x, y, z);
                if (edgeTable[cubeIndex] & 16)
                    vertList[4] = interpolateVertex(x, y, z + 1, x + 1, y, z + 1);
                if (edgeTable[cubeIndex] & 32)
                    vertList[5] = interpolateVertex(x + 1, y, z + 1, x + 1, y + 1, z + 1);
                if (edgeTable[cubeIndex] & 64)
                    vertList[6] = interpolateVertex(x + 1, y + 1, z + 1, x, y + 1, z + 1);
                if (edgeTable[cubeIndex] & 128)
                    vertList[7] = interpolateVertex(x, y + 1, z + 1, x, y, z + 1);
                if (edgeTable[cubeIndex] & 256)
                    vertList[8] = interpolateVertex(x, y, z, x, y, z + 1);
                if (edgeTable[cubeIndex] & 512)
                    vertList[9] = interpolateVertex(x + 1, y, z, x + 1, y, z + 1);
                if (edgeTable[cubeIndex] & 1024)
                    vertList[10] = interpolateVertex(x + 1, y + 1, z, x + 1, y + 1, z + 1);
                if (edgeTable[cubeIndex] & 2048)
                    vertList[11] = interpolateVertex(x, y + 1, z, x, y + 1, z + 1);

                // Create triangles
                for (int i = 0; triTable[cubeIndex][i] != -1; i += 3) {
                    uint32_t baseIndex = static_cast<uint32_t>(outPositions.size());

                    glm::vec3 v0 = vertList[triTable[cubeIndex][i]];
                    glm::vec3 v1 = vertList[triTable[cubeIndex][i + 1]];
                    glm::vec3 v2 = vertList[triTable[cubeIndex][i + 2]];

                    // Calculate face normal
                    glm::vec3 edge1 = v1 - v0;
                    glm::vec3 edge2 = v2 - v0;
                    glm::vec3 normal = glm::normalize(glm::cross(edge1, edge2));

                    outPositions.push_back(v0);
                    outPositions.push_back(v1);
                    outPositions.push_back(v2);

                    outNormals.push_back(normal);
                    outNormals.push_back(normal);
                    outNormals.push_back(normal);

                    outIndices.push_back(baseIndex);
                    outIndices.push_back(baseIndex + 1);
                    outIndices.push_back(baseIndex + 2);
                }
            }
        }
    }
}

